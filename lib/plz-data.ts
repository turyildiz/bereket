// Search function - searches by PLZ or City name
export function searchPLZ(query: string, limit: number = 5): PLZEntry[] {
    if (!query || query.length < 2) return [];

    const normalizedQuery = query.toLowerCase().trim();
    const isNumeric = /^\d+$/.test(normalizedQuery);

    const results: PLZEntry[] = [];
    const seenCombinations = new Set<string>();

    for (const entry of plzData) {
        if (results.length >= limit) break;

        const matches = isNumeric
            ? entry.plz.startsWith(normalizedQuery)
            : entry.city.toLowerCase().includes(normalizedQuery);

        if (matches) {
            const key = `${entry.plz}-${entry.city}`;
            if (!seenCombinations.has(key)) {
                seenCombinations.add(key);
                results.push(entry);
            }
        }
    }

    return results;
}

// Get unique cities (for dropdown without duplicates)
export function getUniqueCities(query: string, limit: number = 5): PLZEntry[] {
    if (!query || query.length < 2) return [];

    const normalizedQuery = query.toLowerCase().trim();
    const isNumeric = /^\d+$/.test(normalizedQuery);

    const results: PLZEntry[] = [];
    const seenCities = new Set<string>();

    for (const entry of plzData) {
        if (results.length >= limit) break;

        const matches = isNumeric
            ? entry.plz.startsWith(normalizedQuery)
            : entry.city.toLowerCase().includes(normalizedQuery);

        // For city searches, dedupe by city name; for PLZ keep all
        if (matches) {
            if (isNumeric) {
                results.push(entry);
            } else if (!seenCities.has(entry.city)) {
                seenCities.add(entry.city);
                results.push(entry);
            }
        }
    }

    return results;
}
